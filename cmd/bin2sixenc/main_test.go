package main

import (
	"reflect"
	"testing"
)

func Test_bin2sixenc(t *testing.T) {
	type args struct {
		bs []rune
	}
	tests := []struct {
		name string
		args args
		want []rune
	}{
		{"11000100101000110000000111000111000011010011011011001011001100101001001000001011000111010011000111000010110111000011100010000010 -> 1JLA104623JRB1410-08", args{[]rune("11000100101000110000000111000111000011010011011011001011001100101001001000001011000111010011000111000010110111000011100010000010")}, []rune("1JLA104623JRB1410-08")},
		{"11011100001000000100001000001100010011000111001011001111010011010111011011011110 -> 7BABCD1234567", args{[]rune("11011100001000000100001000001100010011000111001011001111010011010111011011011110")}, []rune("7BABCD1234567")},
	}
	for _, tt := range tests {
		t.Run(tt.name, func(t *testing.T) {
			if got := bin2sixenc(tt.args.bs); !reflect.DeepEqual(got, tt.want) {
				t.Errorf("bin2sixenc() = %v, want %v", string(got), string(tt.want))
			}
		})
	}
}
